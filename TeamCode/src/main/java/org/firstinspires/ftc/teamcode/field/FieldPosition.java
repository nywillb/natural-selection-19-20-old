package org.firstinspires.ftc.teamcode.field;

/**
 * Field position shows the position of the robot on the field
 */
public enum FieldPosition {
    /**
     * Red 1 is the space farther from the audience on the red side
     */
    RED_1,

    /**
     * Red 2 is the space closer to the audience on the red side
     */
    RED_2,

    /**
     * Blue 1 is the space farther from the audience on the blue side
     */
    BLUE_1,

    /**
     * Blue 2 is the space farther from the audience on the blue side
     */
    BLUE_2
}
